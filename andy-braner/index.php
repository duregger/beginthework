							<h2>A Conversation with Andy Braner</h2>
                             <hr>
                             <p>Andy currently resides in Washington D.C. and has spent the last few years focused on helping people come together in business, education, and social work to achieve a common understanding of the human condition. He has spent time representing the United States overseeing foreign elections and continues to consult on international affairs. He has served in the following positions:<br/>
							  	<ul>
								  <li>- Senior Congressional Fellow at Transatlantic Leadership Network</li>
								  <li>- United States House of Representatives – Fellow International Affairs</li>
								  <li>- John’s Hopkins Center Fellow for Transatlantic Relations</li>
								</ul></p>
                                
                                <p><strong>Summary</strong></br>
	                                Sam and Andy go back 14+ years, as Sam worked as the Waterfront Coordinator and Programs Director for Andy who was the Executive Director of Kanakuck Colorado (which became Camp Kivu), in 2005-2007. In this episode, Andy and Sam look back on their time in youth ministry and connect the dots from #camplife to #govlife, as they now both work for and in Government -- Andy in Washington D.C. acting as a Special Advisor for Congressman Jeff Fortenberry of Nebraska. Sam, as the State Purchasing Director for the State of Oklahoma.</p>
