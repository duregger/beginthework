							<h2>A Conversation with Todd Smith</h2>
                             <hr>
                             
							 <p><strong>Todd Smith</strong><br/>
							 Todd Smith, formerly the President and Chief Marketing Officer (CMO) of <a href="http://sonicdrivein.com" target="_blank">SONIC Drive-in</a>, currently lives in Salt Lake City, Utah with his wife and five kids. Todd is the Chief Creative Officer (CCO) of <a href="https://www.caferio.com/" target="_blank">Cafe Rio</a> and franchisee of 4 <a href="https://www.corelifeeatery.com/" target="_blank">CoreLife Eateries</a> across the Salt Lake Valley. Todd has over 13 years of restaurant experience, with executive experience at Yum! Brands, Wendy’s and Sonic Drive-in. Todd graduated from BYU and got his masters from Northwestern.</p>
                                
                                <hr>
                                <p><strong>Summary</strong></br>
	                                In this episode, Todd and I look back on our time at SONIC Drive-In, where Todd led the digital innovation vision as CMO and later President of SONIC. The focus in the first portion of the conversation is how as an executive Todd began the process of preparing room for digital innovation and then the conversation moves to when I joined the team and the work began in earnest to change, from the inside out, a legacy brand with digital aspirations. Pulling out lessons learned in the process of taking a Quick Service Restaurant (QSR) from little to no digital footprint to an industry leader in digital experience with an omni-channel personalized order ahead experience (web, mobile, kiosks) across 80,000 POPS kiosks in 3,600 stores throughout the US.</p>
	                                </div>
	                                </div>
	                                <div class="col-xl-10 col-lg-10">
                        <div class="about-right">
	                        
	                                <p>I spent some time picking Todd's brain on how he thought about and tackled the politics of change from an executive point-of-view, looking to demystify the tactics and conversations happening behind closed doors with top executives. Todd speaks with candor and humility in the obstacles and successes he found in digital transformation of a $4B publicly traded company.</p>
	                            <hr>
	                            <p><strong>Inspire Acquisition</strong><br/>
							 Brought up by Sam midway through the episode, Paul Brown of Inspire brands has talked a lot about why they chose to purchase SONIC and it has been cited a few times the technology innovation and digital customer experience as a big reason SONIC was a desirable purchase. Many of these conversations were made in all employee meetings, but a few were cited in article <a href="https://www.qsrmagazine.com/finance/arbys-parent-acquires-sonic-23-billion" target="_blank">like this one</a>.<br/><br/>
								 
								<a href="https://stories.inspirebrands.com/video-inspire-brands-completes-acquisition-sonic-drive-in/" target="_blank">More on the acquisition</a><br/>
								<a href="http://samduregger.com/digital/order-ahead-case-studies" target="_blank">More on Order Ahead</a>
								</p>
								<p><iframe width="560" height="315" src="https://www.youtube.com/embed/SxYQr_JRgCA" frameborder="0" allow="accelerometer; autoplay; encrypted-media; gyroscope; picture-in-picture" allowfullscreen></iframe><br/>A full length demo of the omni-channel Order Ahead experience at SONIC Drive-In</p>
							  <hr>
							  
								<p><strong>Patience and Drive</strong><br/>
								Todd brings this up as a lesson he applied while at SONIC and now in his new endeavors. Whether in corporate life or startup life, two virtues or characteristics that Todd most values are Patience and Drive.<br/><br/>When held in tandem they can be a devastating tool versus any obstacle in your way. If your patient you will not get frustrated when the going gets slow, and if you've a bit of drive, you can feel through the stagnant moments and find room to accelerate when the timing is right. I loved this perspective from Todd, as I saw him do this so many times, but he had never quite described the motivation behind his leadership rhythm -- this puts words to the action I witnessed.   
								</p>
							 <hr>
							 <p><strong>Don't let your title fool you...</strong><br/>
								Near the end of the conversation, Todd drops a whole jam-packed taco of wisdom on us, he says:</p>
								<p><blockquote>"Don't confuse your title with who you are."</blockquote></p>
								<p><blockquote>"Some leaders become so self-important, that they forget how to do."</blockquote></p>
								<p> This coming from a guy who had a seven-figure salary (public company filings) and all the reason in the world to not have to "DO" anything. As I continue my career, it is a breathe of fresh air to hear Todd's view on this, as people who forget how things are done become out of touch with the day-to-day struggle of work and by default the workers. When this happens empathy for the workforce is diluted and decisions are more self-centered than altruistic [my words, not Todd's].</p>
