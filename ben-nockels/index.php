                                <p><strong>Summary</strong></br>
	                                In our inaugural interview Sam sits down with Ben Nockels of Commonplace Books to discuss Commonplace's origin story and what's next for the brand, in a fully packed 90 minute conversation. Sam and Ben go back 11 or so years, and have grown their friendship in hallway conversation form. This is actually one of the first times they have sat down to chat in a formal conversation... which is astonishing fact given their long history. Many of the best conversations came in snippets and extended moments as they each have had many intersecting projects and pursuits over the last decade.</p>
	                                
